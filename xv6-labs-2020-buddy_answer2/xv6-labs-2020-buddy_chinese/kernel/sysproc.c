uint64 sys_kinit(void) {
  kinit();
  return 0;  // 返回 0 表示成功
}

int sys_kfree(void) {
  char pa[128];  // 为字符串分配一个缓冲区，假设最大长度为128字节
  if (argstr(0, pa, sizeof(pa)) < 0) {  // 获取字符串
    return -1;  // 获取参数失败
  }
  kfree(pa);  // 释放内存
  return 0;  // 成功
}

uint64 sys_kalloc(void) {
  void *pa = kalloc();
  if (pa == 0) {
    return -1;  // 返回 -1 表示分配失败
  }
  return (uint64)pa;  // 返回分配的内存地址
}
